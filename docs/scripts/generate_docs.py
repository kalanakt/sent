def main() -> None:
    print("Generating sent API documentation...")

    # Client API
    client_dir = PAGES_DIR / "api" / "client"
    _write(client_dir / "index.mdx", generate_client_index())
    for section_id, config in CLIENT_SECTIONS.items():
        _write(client_dir / f"{section_id}.mdx", generate_client_section(section_id, config))
    print(f"  Client API: {len(CLIENT_SECTIONS) + 1} pages")

    # Events
    events_index, event_sidebar, event_pages = generate_events()
    events_dir = PAGES_DIR / "api" / "events"
    _write(events_dir / "index.mdx", events_index)
    for slug, content in event_pages:
        _write(events_dir / f"{slug}.mdx", content)
    print(f"  Events: {1 + len(event_pages)} pages")

    # Sessions & Errors
    _write(PAGES_DIR / "api" / "sessions.mdx", generate_sessions())
    _write(PAGES_DIR / "api" / "errors.mdx", generate_errors())
    button_mdx, conv_mdx = generate_helpers()
    _write(PAGES_DIR / "api" / "helpers" / "button.mdx", button_mdx)
    _write(PAGES_DIR / "api" / "helpers" / "conversation.mdx", conv_mdx)
    print("  Sessions, Errors, Helpers: 4 pages")

    # TL reference
    tl_types_dir = PAGES_DIR / "tl" / "types"
    tl_funcs_dir = PAGES_DIR / "tl" / "functions"
    tl_type_ns: list[str] = []
    tl_func_ns: list[str] = []

    for path in sorted(_tl_types_dir().glob("*.py")):
        if path.name in ("__init__.py", "all.py"):
            continue
        ns = path.stem
        tl_type_ns.append(ns)
        _write(tl_types_dir / f"{ns}.mdx", generate_tl_namespace("types", path, ns))
    all_path = _tl_types_dir() / "all.py"
    if all_path.exists():
        _write(tl_types_dir / "mtproto.mdx", generate_tl_namespace("types", all_path, "all"))
        tl_type_ns.append("mtproto")

    for path in sorted(_tl_functions_dir().glob("*.py")):
        if path.name in ("__init__.py", "all.py"):
            continue
        ns = path.stem
        tl_func_ns.append(ns)
        _write(tl_funcs_dir / f"{ns}.mdx", generate_tl_namespace("functions", path, ns))

    all_func = _tl_functions_dir() / "all.py"
    if all_func.exists():
        tl_func_ns.append("mtproto")
        _write(tl_funcs_dir / "mtproto.mdx", generate_tl_namespace("functions", all_func, "all"))

    print(f"  TL types: {len(tl_type_ns)} namespaces")
    print(f"  TL functions: {len(tl_func_ns)} namespaces")

    # Sidebar
    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    sidebar = generate_sidebar(tl_type_ns, tl_func_ns, event_sidebar)
    (GENERATED_DIR / "sidebar.json").write_text(json.dumps(sidebar, indent=2), encoding="utf-8")
    print("  sidebar.json written")
    print("Done.")


if __name__ == "__main__":
    main()
