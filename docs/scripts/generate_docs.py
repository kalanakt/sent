def _find_class(classes: list[ClassInfo], name: str) -> ClassInfo | None:
    for c in classes:
        if c.name == name:
            return c
    return None


def _methods_for_class(path: Path, class_name: str) -> list[MethodInfo]:
    cls = _find_class(parse_module(path), class_name)
    if not cls:
        return []
    return [m for m in cls.methods if not m.name.startswith("_") or m.name in ("__call__",)]


def _write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(GENERATED_BANNER + content, encoding="utf-8")


def _tl_link(method_name: str) -> str:
    if method_name not in TL_METHOD_MAP:
        return ""
    ns, cls = TL_METHOD_MAP[method_name]
    kind = ns.split(".")[-1]
    return f"\n\n**Underlying TL:** [`{cls}`](/tl/{kind}/{ns.split('.')[-1] if kind == 'functions' else kind}#{cls.lower()})"


def generate_client_section(section_id: str, config: dict) -> str:
    lines = [f"# {config['title']}", ""]
    all_methods: list[MethodInfo] = []

    if "methods" in config:
        for fname in config["files"]:
            path = _client_dir() / fname
            classes = parse_module(path)
            for cls_name, method_names in config["methods"].items():
                cls = _find_class(classes, cls_name)
                if not cls:
                    continue
                for m in cls.methods:
                    if m.name in method_names:
                        all_methods.append(m)
    elif "classes" in config:
        for fname in config["files"]:
            path = _client_dir() / fname
            classes = parse_module(path)
            for cls_name in config["classes"]:
                all_methods.extend(_methods_for_class(path, cls_name))
    else:
        fname = config["files"][0]
        path = _client_dir() / fname
        all_methods = _methods_for_class(path, config["class"])

    for method in all_methods:
        example = METHOD_EXAMPLES.get(method.name, "")
        lines.append(mdx_method_section(method, example))
        if method.name in TL_METHOD_MAP:
            ns, tl_cls = TL_METHOD_MAP[method.name]
            kind = "functions" if "functions" in ns else "types"
            ns_name = ns.split(".")[-1]
            lines.append(f"**Underlying TL:** [`{tl_cls}`](/tl/{kind}/{ns_name}#{tl_cls.lower()})")
            lines.append("")

    return "\n".join(lines)


