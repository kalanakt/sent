def generate_events() -> tuple[str, list[dict]]:
    index_lines = [
        "# Events",
        "",
        "Register handlers with `@client.on(...)` or `client.add_event_handler(...)`.",
        "",
        "## Event builders",
        "",
        "| Builder | Description |",
        "|---------|-------------|",
    ]
    sidebar_items = [{"text": "Overview", "link": "/api/events"}]
    pages: list[tuple[str, str]] = []

    descriptions = {
        "NewMessage": "Fired when a new message is sent or received.",
        "MessageEdited": "Fired when a message is edited.",
        "MessageDeleted": "Fired when messages are deleted.",
        "MessageRead": "Fired when messages are marked as read.",
        "CallbackQuery": "Fired when an inline keyboard button is pressed.",
        "InlineQuery": "Fired when a bot receives an inline query.",
        "ChatAction": "Fired on join, leave, title change, and similar actions.",
        "Raw": "Receives unfiltered raw TL updates.",
        "UserUpdate": "Fired on user status, name, or typing changes.",
        "Album": "Groups media sent as an album into one event.",
    }

    for fname in EVENT_FILES:
        path = _events_dir() / fname
        classes = parse_module(path)
        builders = [c for c in classes if c.name not in ("EventCommon", "EventBuilder") and not c.name.endswith("Event")]
        for builder in builders:
            slug = slugify(builder.name)
            desc = descriptions.get(builder.name, builder.docstring or "")
            index_lines.append(f"| [`{builder.name}`](/api/events/{slug}) | {desc} |")
            sidebar_items.append({"text": builder.name, "link": f"/api/events/{slug}"})

            page = [f"# {builder.name}", "", desc, ""]
            init_method = next((m for m in builder.methods if m.name == "__init__"), None)
            if init_method:
                page.append("## Filters")
                page.append("")
                page.append(mdx_method_section(init_method))
            event_cls = next(
                (c for c in classes if c.name == f"{builder.name}Event"),
                None,
            )
            if event_cls:
                page.extend(["## Event instance", ""])
                for m in event_cls.methods:
                    if not m.name.startswith("_"):
                        page.append(mdx_method_section(m))
                for slot in event_cls.slots:
                    page.append(f"- `{slot}`")
                page.append("")
            elif builder.name == "Album":
                page.extend(
                    [
                        "## Event instance",
                        "",
                        "Returns a list of [`MessageEvent`](/api/events/newmessage) objects for grouped media.",
                        "",
                    ]
                )
            pages.append((slug, "\n".join(page)))

    common = parse_module(_events_dir() / "common.py")
    eb = _find_class(common, "EventBuilder")
    if eb:
        init = next((m for m in eb.methods if m.name == "__init__"), None)
        if init:
            index_lines.extend(["", "## Common filters (`EventBuilder`)", ""])
            index_lines.append(mdx_method_section(init))

    index_lines.extend(
        [
            "",
            "## Registration",
            "",
            "```python",
            "@client.on(events.NewMessage(incoming=True, pattern=r'hello'))",
            "async def handler(event):",
            "    await event.reply('Hi!')",
            "",
            "await client.run_until_disconnected()",
            "```",
        ]
    )

    return "\n".join(index_lines), sidebar_items, pages


def generate_sessions() -> str:
    lines = [
        "# Sessions",
        "",
        "Sessions store the authorization key, datacenter, entity cache, and update state.",
        "",
        "| Class | Description |",
        "|-------|-------------|",
        "| `SQLiteSession` | Default file-backed session (`name.session`) |",
        "| `StringSession` | Portable base64 string (starts with `1`) |",
        "| `MemorySession` | In-memory, not persisted |",
        "| `Session` | Abstract base for custom implementations |",
        "",
    ]
    for fname in ["abstract.py", "sqlite.py", "string.py", "memory.py"]:
        path = _sessions_dir() / fname
        for cls in parse_module(path):
            lines.extend([f"## `{cls.name}`", ""])
            if cls.docstring:
                lines.append(cls.docstring)
                lines.append("")
            if cls.bases:
                lines.append(f"**Bases:** `{', '.join(cls.bases)}`")
                lines.append("")
            for m in cls.methods:
                if m.name.startswith("_"):
                    continue
                lines.append(mdx_method_section(m))
    return "\n".join(lines)


def generate_errors() -> str:
    lines = [
        "# Errors",
        "",
        "All sent exceptions inherit from `SentError`. RPC errors inherit from `RPCError`.",
        "",
        "## Hierarchy",
        "",
        "```",
        "SentError",
        "├── AuthKeyError",
        "│   └── AuthKeyUnregisteredError",
        "├── SessionPasswordNeededError",
        "└── RPCError",
        "    ├── FloodWaitError",
        "    ├── PhoneCodeInvalidError",
        "    └── ... (see below)",
        "```",
        "",
        "## Exception reference",
        "",
    ]
    for fname in ["common.py", "rpcerrorlist.py"]:
        path = _errors_dir() / fname
        for cls in parse_module(path):
            if cls.name == "SentError":
                continue
            desc = cls.docstring or "Telegram RPC error."
            lines.extend([f"### `{cls.name}`", "", desc, ""])
            init = next((m for m in cls.methods if m.name == "__init__"), None)
            if init and len(init.params) > 1:
                lines.append(mdx_method_section(init))
    lines.extend(
        [
            "## Handling flood waits",
            "",
            "```python",
            "from sent.errors import FloodWaitError",
            "",
            "try:",
            "    await client.send_message(chat, text)",
            "except FloodWaitError as e:",
            "    await asyncio.sleep(e.seconds)",
            "```",
            "",
            "Set `flood_sleep_threshold` on the client to auto-sleep for short waits.",
        ]
    )
    return "\n".join(lines)


def generate_helpers() -> tuple[str, str]:
    button_path = _client_dir() / "buttons.py"
    conv_path = _client_dir() / "conversation.py"

    button_lines = ["# Button", "", "Build inline and reply keyboards.", ""]
    for cls in parse_module(button_path):
        if cls.name == "Button":
            for m in cls.methods:
                if not m.name.startswith("_"):
                    button_lines.append(mdx_method_section(m))

    button_lines.extend(
        [
            "## Example",
            "",
            "```python",
            "from sent.client.buttons import Button",
            "",
            "buttons = Button.build([",
            "    [Button.inline('Yes', data=b'yes'), Button.inline('No', data=b'no')],",
            "])",
            "await client.send_message(chat, 'Confirm?', buttons=buttons)",
            "```",
        ]
    )

    conv_lines = ["# Conversation", "", "Wait for replies in a chat.", ""]
    for cls in parse_module(conv_path):
        if cls.name == "Conversation":
            if cls.docstring:
                conv_lines.extend([cls.docstring, ""])
            for m in cls.methods:
                if not m.name.startswith("_") or m.name in ("__aenter__", "__aexit__"):
                    example = METHOD_EXAMPLES.get("conversation", "") if m.name == "__aenter__" else ""
                    conv_lines.append(mdx_method_section(m, example))

    return "\n".join(button_lines), "\n".join(conv_lines)


def generate_tl_namespace(kind: str, path: Path, namespace: str) -> str:
    import_path = f"sent.tl.{kind}.{namespace}"
    title = f"TL {'Types' if kind == 'types' else 'Functions'}: `{namespace}`"
    lines = [f"# {title}", "", f"Import from `{import_path}`.", "", "## Contents", ""]
    classes = parse_module(path)
    for cls in classes:
        if cls.name.startswith("_"):
            continue
        anchor = cls.name.lower()
        lines.append(f"- [{cls.name}](#{anchor})")
    lines.append("")

    for cls in classes:
        if cls.name.startswith("_"):
            continue
        lines.extend([f"## {cls.name}", ""])
        if cls.constructor_id is not None:
            lines.append(f"**Constructor ID:** `{cls.constructor_id}`")
            lines.append("")
        if cls.slots:
            lines.append(f"**Fields:** `{', '.join(f'`{s}`' for s in cls.slots)}`")
            lines.append("")
        init = next((m for m in cls.methods if m.name == "__init__"), None)
        if init:
            lines.append("```python")
            lines.append(format_signature(init, prefix=f"{cls.name}."))
            lines.append("```")
            lines.append("")
            lines.append(params_table_simple(init))
            lines.append("")
        lines.extend(
            [
                "```python",
                f"from {import_path} import {cls.name}",
                "```",
                "",
            ]
        )
        if kind == "functions":
            lines.extend(
                [
                    "```python",
                    f"result = await client({cls.name}(...))",
                    "```",
                    "",
                ]
            )
    return "\n".join(lines)


def params_table_simple(method: MethodInfo) -> str:
    from ast_utils import params_table

    return params_table(method)


